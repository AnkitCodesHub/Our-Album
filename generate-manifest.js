#!/usr/bin/env node
/**
 * Scans /photos and (re)writes data.js.
 *
 *   - New images are added automatically, sorted by file modified date
 *   - Captions and dates you've already written are preserved (matched by filename)
 *
 * Usage:
 *   node generate-manifest.js
 *
 * Requires Node.js (no extra packages). Run this on your own computer,
 * inside this project folder, after adding photos to /photos.
 */
const fs = require("fs");
const path = require("path");

const PHOTOS_DIR = path.join(__dirname, "photos");
const DATA_FILE = path.join(__dirname, "data.js");
const VALID_EXT = [".jpg", ".jpeg", ".png", ".webp", ".heic", ".heif"];

function loadExisting() {
  if (!fs.existsSync(DATA_FILE)) return {};
  const content = fs.readFileSync(DATA_FILE, "utf8");
  const match = content.match(/window\.ALBUM_PHOTOS\s*=\s*(\[[\s\S]*?\]);/);
  if (!match) return {};
  try {
    // eslint-disable-next-line no-eval
    const arr = eval(match[1]);
    const map = {};
    arr.forEach((p) => {
      if (p && p.src) map[p.src] = p;
    });
    return map;
  } catch {
    return {};
  }
}

function main() {
  if (!fs.existsSync(PHOTOS_DIR)) {
    console.error("❌ No /photos folder found next to this script.");
    process.exit(1);
  }

  const existing = loadExisting();

  const files = fs
    .readdirSync(PHOTOS_DIR)
    .filter((f) => VALID_EXT.includes(path.extname(f).toLowerCase()));

  if (files.length === 0) {
    console.error("❌ No images found in /photos. Add some, then run this again.");
    process.exit(1);
  }

  const entries = files.map((file) => {
    const stat = fs.statSync(path.join(PHOTOS_DIR, file));
    const prior = existing[file];
    return {
      src: file,
      date: prior ? prior.date : stat.mtime.toISOString().slice(0, 10),
      caption: prior ? prior.caption || "" : "",
      _mtime: stat.mtime.getTime(),
    };
  });

  entries.sort((a, b) => a.date.localeCompare(b.date) || a._mtime - b._mtime || a.src.localeCompare(b.src));

  const lines = entries.map((e) => {
    const cap = String(e.caption).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    return `  { src: "${e.src}", date: "${e.date}", caption: "${cap}" },`;
  });

  const output = `// 📷 Auto-generated by generate-manifest.js — safe to hand-edit after.
// Re-running the script preserves captions/dates you've already set, by filename.
//
//   src      — filename inside /photos
//   date     — YYYY-MM-DD, drives the chronological grouping (defaults to the
//              file's last-modified date — fix any that came out wrong)
//   caption  — optional. Add text to feature a photo as a bigger "moment".

window.ALBUM_PHOTOS = [
${lines.join("\n")}
];
`;

  fs.writeFileSync(DATA_FILE, output);
  console.log(`✅ Wrote ${entries.length} photo${entries.length === 1 ? "" : "s"} to data.js`);
  console.log("   Open data.js to add captions to your favorites, then refresh index.html.");
}

main();
